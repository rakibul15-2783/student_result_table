 function calculation1() {
     var _thm1 = document.getElementById('thm1').value;
     var _prac1 = document.getElementById('prac1').value;
     var _tm = parseInt(_thm1) + parseInt(_prac1);
     document.getElementById('tm1').value = _tm;


     if (_tm >= 80 && _tm <= 100) {
         var _g1 = document.getElementById('g1').value = "A+";
         var _p1 = document.getElementById('p1').value = "5.00";
     } else if (_tm >= 70 && _tm <= 79) {
         var _g1 = document.getElementById('g1').value = "A";
         var _p1 = document.getElementById('p1').value = "4.00";
     } else if (_tm >= 60 && _tm <= 69) {
         var _g1 = document.getElementById('g1').value = "A-";
         var _p1 = document.getElementById('p1').value = "3.50";
     } else if (_tm >= 50 && _tm <= 59) {
         var _g1 = document.getElementById('g1').value = "B";
         var _p1 = document.getElementById('p1').value = "3.00";
     } else if (_tm >= 40 && _tm <= 49) {
         var _g1 = document.getElementById('g1').value = "C";
         var _p1 = document.getElementById('p1').value = "2.00";
     } else if (_tm >= 33 && _tm <= 39) {
         var _g1 = document.getElementById('g1').value = "D";
         var _p1 = document.getElementById('p1').value = "1.00";
     } else if (_tm >= 0 && _tm <= 32) {
         var _g1 = document.getElementById('g1').value = "F";
         var _p1 = document.getElementById('p1').value = "0.00";
     } else {
         alert("Enter something wrong!");
     }
 }


